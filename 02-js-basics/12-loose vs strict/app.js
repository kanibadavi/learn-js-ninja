const num = 23;

//loose comparison(different types can be equal)

console.log(num == 25);
console.log(num == '25');
console.log(num != 25);
console.log(num != '25');

//strict comparison(differrent types cannot be equal)

console.log(num === 25);
console.log(num === '25');
console.log(num !== 25);
console.log(num !== '25');